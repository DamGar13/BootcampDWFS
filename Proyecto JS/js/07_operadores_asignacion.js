/* operadores de asignacion */

/* operador de asignacion = */
let puntaje = 0;
puntaje = 100;
console.log('puntaje inicial: ' + puntaje);

/* operador asignacion con suma */

puntaje += 50; //--- es igual a escribir puntaje = puntaje + 50
console.log('puntaje actual: ' + puntaje);


/* operador asignacion con resta */

puntaje -= 10; //--- es igual a escribir puntaje = puntaje - 10
console.log('puntaje actual: ' + puntaje);

/* operador asignacion con multiplicacion */

puntaje *= 5; //--- es igual a escribir puntaje = puntaje * 5
console.log('puntaje actual: ' + puntaje);

/* operador asignacion con division */

puntaje /= 3; //--- es igual a escribir puntaje = puntaje / 3
console.log('puntaje actual: ' + puntaje);

