/* funciones */

// declarar funcion

function sumarNumerosUno(){
	let numero1 = 10;
	let numero2 = 15;
	let resultado = 0;
	resultado = numero1 + numero2;
	console.log(`${numero1} + ${numero2} = ${resultado}`);
}

//--- llamado de la funcion

sumarNumerosUno();

// declarar funcion

function sumarNumerosDos(numero1, numero2){
	let resultado = 0;
	resultado = numero1 + numero2;
	console.log(`${numero1} + ${numero2} = ${resultado}`);
}

//--- llamado de la funcion

sumarNumerosDos(15, 50);
sumarNumerosDos(10, 20);
sumarNumerosDos(20, 1);

