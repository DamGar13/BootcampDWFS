/* operadores de comparacion */

let numeroUno = 10;
let numeroDos = 5;

/*operador de igualdad ==*/

console.log(numeroUno == numeroDos); //--- solo devuelve true o false

/*operador de igualdad estricto ===*/

numeroDos = '10'
console.log(numeroUno === numeroDos); //--- solo devuelve true o false

/* operador de diferencia != */

console.log(numeroUno != numeroDos); //--- solo devuelve true o false

/* operador de diferencia estricto !== */

console.log(numeroUno !== numeroDos); //--- solo devuelve true o false

/* operador mayor que > */

numeroUno = 15
console.log(numeroUno > numeroDos); //--- solo devuelve true o false

/* operador mayor o igual que >= */

console.log(numeroUno >= numeroDos); //--- solo devuelve true o false

/* operador menor < */

console.log(numeroUno < numeroDos); //--- solo devuelve true o false

/* operador menor o igual que <= */

console.log(numeroUno <= numeroDos); //--- solo devuelve true o false
